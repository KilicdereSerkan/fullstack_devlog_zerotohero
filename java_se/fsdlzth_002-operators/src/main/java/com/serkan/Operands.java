package com.serkan;

public class Operands {
    static void main() {
        // Operands are the values or variables on which operators perform operations.
        int a = 10;
        int b = 3;
        // In the expression (a + b), 'a' and 'b' are operands for the '+' operator.
        System.out.println("Addition: " + (a + b)); // 13
        // In the expression (a > b), 'a' and 'b' are operands
        System.out.println("Greater Than: " + (a > b)); // true

    }
}
